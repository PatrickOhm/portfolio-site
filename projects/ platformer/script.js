rectangle = {
    height: 20,
    jumping: true,
    width: 15,
    x: 2 * size,
    x_old: 0,
    x_velocity: 0,
    y: 28 * size,
    y_old: 0,
    y_velocity: 0
};


controller = {
    left: false,
    right: false,
    up: false,
    keyListener: function(event) {
        var key_state = (event.type == 'keydown')?true:false;
        console.log(event.keyCode);

        switch(event.keyCode) {
            case 37: controller.left = key_state; break;
            case 38: controller.up = key_state; break;
            case 39: controller.right = key_state;  break;
            case 32: start = true;
        }

    }
};

collider = {
    offset: 0.001,

    1:function(object, column, row) {
        let valueLower = map[level][(row + 1) * columns + column];
        let valueUpper = map[level][(row - 1) * columns + column];
        if(valueUpper != 1) {
            if(this.collideTop(object, row)) return;
        }
        
        
        if (object.y + object.height > (row - 1) * size + size) {

            if (this.collideLeft(object, column)) return;
            if (this.collideRight(object, column)) return;
  
          }
        
        this.collideBottom(object,row);
        
    },

    2:function(object, column, row) {
        if (level == 5) {
            rectangle.x = 1 * size;
            rectangle.y = 9 * size - 2;
            return;
        }

        if (level == 7) {
            rectangle.x = 48 * size;
            rectangle.y = 28 * size - 2;
            return;
        }
        
        rectangle.x = 1 * size;
        rectangle.y = 28 * size;
            

    },

    3:function(object,column, row) {
        if (level < 9) level++;
        if (level == 9) drawEndScreen(canvas);
        
        
        p = 1;
        loading = true;

        if (level == 5) {
            rectangle.x = 1 * size;
            rectangle.y = 9 * size - 2;
            return;
        }

        if (level == 7) {
            rectangle.x = 48 * size;
            rectangle.y = 28 * size - 2;
            return;
        }
        rectangle.x = 1 * size;
        rectangle.y = 28 * size;
    },

    collideBottom:function(object, row, y_offset = size) {
        let bottom = row * size + y_offset;
        

        if(object.y < bottom && object.y_old >= bottom) {
            object.y_velocity = 0;
            object.y = bottom + this.offset;
            

            return true;
        }

        return false;
    },

    collideTop:function(object, row, y_offset = 0) {
        let top = row * size + y_offset;
        

        if(object.y + object.height > top && object.y_old + object.height <= top) {
            object.jumping = false;
            object.y_velocity = 0;
            object.y = top - object.height - this.offset;
  
            return true;
        }
            return false;
    },


    collideLeft:function(object, column) {
        let left = column * size;

        if (object.x + object.width > left && object.x_old + object.width <= left) {

            object.x_velocity = 0;
            object.x = left - object.width - this.offset;
            
            
  
            return true;
  
          } return false;
    },

    collideRight:function(object, column) {

        let right = column * size + size;

        if (object.x < right && object.x_old >= right) {

          object.x_velocity = 0;
          object.x = right;
          
          

          return true;

        } return false;

      },





    handleCollision:function(object, map) {
        let column, row, value;

        // TEST BOTTOM

        column = Math.floor(object.x / size); // column under the left side
        row = Math.floor((object.y + object.height) / size); // the row under the player
        value = map[level][row * columns + column];
        
        if (value != 0) this[value](object,column,row);

        column = Math.floor((object.x + object.width) / size); // column under the right side
        value = map[level][row * columns + column];

        if (value != 0) this[value](object,column,row);

        // TEST TOP

        column = Math.floor(object.x / size);// The column under the left side of the object:
        row    = Math.floor(object.y / size);// The row under the top side of the object:
        value  = map[level][row * columns + column];// We get the tile value under the top left corner of the object:

        if (value != 0) this[value](object, column, row);

        //TEST LEFT

        column = Math.floor(object.x / size);// The column under the left side of the object:
        row    = Math.floor(object.y / size);// Top side row:
        value  = map[level][row * columns + column];

        if (value != 0) this[value](object, column, row);

        row = Math.floor((object.y + object.height) / size);// Bottom side row:
        value = map[level][row * columns + column];

        if (value != 0) this[value](object, column, row);

        /* TEST RIGHT */

        column = Math.floor((object.x + object.width) / size);// The column under the right side of the object:
        row    = Math.floor(object.y / size);// Top side row:
        value  = map[level][row * columns + column];

        if (value != 0) this[value](object, column, row);

        row = Math.floor((object.y + object.height) / size);// Bottom side row:
        value = map[level][row * columns + column];

        if (value != 0) this[value](object, column, row);
        
    }

};






loop = function() {
    if (controller.up && rectangle.jumping == false) {
        rectangle.y_velocity -= 20;
        rectangle.jumping = true;
    }

    if (controller.left) {
        rectangle.x_velocity -= 1.5;
    }

    if (controller.right) {
        rectangle.x_velocity += 1.5;
    }

    rectangle.x_old = rectangle.x;
    rectangle.y_old = rectangle.y; 

    rectangle.y_velocity += 1; //gravity
    rectangle.x += rectangle.x_velocity;
    rectangle.y += rectangle.y_velocity;
    rectangle.x_velocity *= 0.8;
    rectangle.y_velocity *= 0.9;


    // collision with the walls
    if (rectangle.x < 0) {
        rectangle.x_velocity = 0;
        rectangle.x = 0;
    } else if (rectangle.x + rectangle.width > 1000 - 0.1) {
        rectangle.x = 1000 - 0.1 - rectangle.width;
        rectangle.x_velocity = 0; 
        
    } 

    if (rectangle.y < 0) {
        rectangle.y_velocity = 0;
        rectangle.y = 0;
    }

    collider.handleCollision(rectangle, map);
    
    drawMap();
    drawPlayer();

    if (start == false)drawStartScreen(context);
    
    if (p < 344) {
        
        if (p > 300) {
            p += Math.random() * (2 - 0) + 0;
        } else {
            p += Math.random() * (3 - 0) + 0;
        }

        if(p > 100 && p < 110) {
            p += 40;
        }

        if(p > 200 && p < 210) {
            p += 40;
        }

        
    }   // for the loading screen
    if (loading == true) drawLoadingScreen(level, context, p);
    if (p > 343) loading = false;
    if (level == 0 && start == true) drawControlInstructions(context);

    

    window.requestAnimationFrame(loop);
};

window.addEventListener('keydown', controller.keyListener);
window.addEventListener('keyup', controller.keyListener);
window.requestAnimationFrame(loop);






